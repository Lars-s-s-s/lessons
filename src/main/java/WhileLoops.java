

public class WhileLoops {
    public static void main(String[] args) {
        boolean t = true;
        boolean d = 5<2;
        System.out.println(t);
        System.out.println(d);

        int val = 0;

        while(val < 5){
            System.out.println(val + 1 + " Loop, " + "val = " + val);
            val = val + 1;
        }

    }
}
