// Lesson 23 - StringBuilder

public class Lesson23 {
    public static void main(String[] args) {
        System.out.println("Привет"); // Переводит строку
        System.out.print("Привет, ещё раз!");  // Не переводит строку
        System.out.println(" Пока");

        System.out.printf("Это строка, %s", "Привет" ); //форматирует строку// %s - подставляет на это место переменную, %d - цифра
        System.out.println();
        System.out.printf("Это строка, %d", 10 );
    }
}
